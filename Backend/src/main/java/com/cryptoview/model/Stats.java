package com.cryptoview.model;

public class Stats {
	private String name;
	private String value;
	
	public Stats() {}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String total_market_cap) {
		this.value = total_market_cap;
	}
	
}
